		if (HitChance > 0 ) then
			self:CastSpell(HK_E,castpos)
		end
	end

	if self:CanCast(_Q) and target.pos:DistanceTo(myHero.pos) < Q.Range and self.Menu.ComboMode.UseQ:Value() then           
        local castpos,HitChance, pos = TPred:GetBestCastPosition(target, Q.Delay , Q.Width, Q.Range,Q.Speed, myHero.pos, Q.ignorecol, Q.Type )
		if (HitChance > 0 ) then
			self:CastSpell(HK_Q,castpos)
		end
	end

	if self:CanCast(_W) and target.pos:DistanceTo(myHero.pos) < W.Range and self.Menu.ComboMode.UseW:Value() then 
		if myHero:GetSpellData(_W).toggleState == 2 and (myHero.health/myHero.maxHealth < self.Menu.ComboMode.WHealth:Value() / 100 ) then
			Control.CastSpell(HK_W)
		end
    end
    
    if self:CanCast(_W) and target.pos:DistanceTo(myHero.pos) < W.Range and self.Menu.ComboMode.UseW:Value() then 
		if myHero:GetSpellData(_W).toggleState == 1 and myHero.mana/myHero.maxMana < .65 and myHero.health/myHero.maxHealth > .75 then
			Control.CastSpell(HK_W)
		end
    end

    if self:CanCast(_R) and target.pos:DistanceTo(myHero.pos) < R.Range and self.Menu.ComboMode.UseR:Value() then 
        if (myHero.health/myHero.maxHealth < self.Menu.ComboMode.RHealth:Value() / 100 ) then
			Control.CastSpell(HK_R)
        end
    end

    if self:EnemyInRange(400) and not self:CanCast(_Q) then 
        local BTarget = CurrentTarget(400)
        if BTarget then
            if myHero.pos:DistanceTo(BTarget.pos) < 400 then
			    UseBotrk()
            end
        end
    end
	end
end

function Aatrox:GetEnemyHeroes()
	self.EnemyHeroes = {}
	for i = 1, Game.HeroCount() do
		local Hero = Game.Hero(i)
		if Hero.isEnemy then
			table.insert(self.EnemyHeroes, Hero)
		end
	end
	return self.EnemyHeroes
end

function Aatrox:EnemyInRange(range)
	local count = 0
	for i, target in ipairs(self:GetEnemyHeroes()) do
		if target.pos:DistanceTo(myHero.pos) < range then 
			count = count + 1
		end
	end
	return count
end

