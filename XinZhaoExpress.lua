function IsRecalling()
	for K, Buff in pairs(GetBuffs(myHero)) do
		if Buff.name == "recall" and Buff.duration > 0 then
			return true
		end
	end
	return false
end

function ValidTarget(target, range)
	range = range and range or math.huge
	return target ~= nil and target.valid and target.visible and not target.dead and target.distance <= range
end

function XinZhao:Tick()
    if myHero.dead or Game.IsChatOpen() == true or IsRecalling() == true then return end
	if self.Menu.HarassMode.harassActive:Value() then
		self:Harass()
	end
	if self.Menu.ComboMode.comboActive:Value() then
		self:Combo()
	end
	if self.Menu.ClearMode.clearActive:Value() then
		self:Jungle()
	end

end

function XinZhao:HasBuff(unit, buffname)
	for i = 0, unit.buffCount do
		local buff = unit:GetBuff(i)
		if buff.name == buffname and buff.count > 0 then 
			return true
		end
	end
	return false
end

function XinZhao:IsReady(spell)
	return Game.CanUseSpell(spell) == 0
end

function XinZhao:CheckMana(spellSlot)
	return myHero:GetSpellData(spellSlot).mana < myHero.mana
end

function XinZhao:CanCast(spellSlot)
	return self:IsReady(spellSlot) and self:CheckMana(spellSlot)
end

function EnableMovement()
	SetMovement(true)
end

function ReturnCursor(pos)
	Control.SetCursorPos(pos)
	DelayAction(EnableMovement,0.1)
end

function LeftClick(pos)
	Control.mouse_event(MOUSEEVENTF_LEFTDOWN)
	Control.mouse_event(MOUSEEVENTF_LEFTUP)
	DelayAction(ReturnCursor,0.05,{pos})
end

function XinZhao:Draw()
    if self:CanCast(_E) then Draw.Circle(myHero.pos, 650, 3,  Draw.Color(255, 000, 222, 255)) end
	if self.Menu.ComboMode.DrawDamage:Value() then
		for i, hero in pairs(self:GetEnemyHeroes()) do
			local barPos = hero.hpBar
			if not hero.dead and hero.pos2D.onScreen and barPos.onScreen and hero.visible then
				local QDamage = (self:CanCast(_Q) and getdmg("Q",hero,myHero) or 0)
				local WDamage = (self:CanCast(_W) and getdmg("W",hero,myHero) or 0)
				local EDamage = (self:CanCast(_E) and getdmg("E",hero,myHero) or 0)
				local damage = QDamage + WDamage + EDamage
				if damage > hero.health then
					Draw.Text("killable", 24, hero.pos2D.x, hero.pos2D.y,Draw.Color(0xFF00FF00))
					
				else
					local percentHealthAfterDamage = math.max(0, hero.health - damage) / hero.maxHealth
					local xPosEnd = barPos.x + barXOffset + barWidth * hero.health/hero.maxHealth
					local xPosStart = barPos.x + barXOffset + percentHealthAfterDamage * 100
					Draw.Line(xPosStart, barPos.y + barYOffset, xPosEnd, barPos.y + barYOffset, 10, Draw.Color(0xFF00FF00))
				end
			end
		end	
	end
end

function XinZhao:CastSpell(spell,pos)
	local customcast = self.Menu.CustomSpellCast:Value()
	if not customcast then
		Control.CastSpell(spell, pos)
		return
	else
		local delay = self.Menu.delay:Value()
		local ticker = GetTickCount()
		if castSpell.state == 0 and ticker > castSpell.casting then
			castSpell.state = 1
			castSpell.mouse = mousePos
			castSpell.tick = ticker
			if ticker - castSpell.tick < Game.Latency() then
				SetMovement(false)
				Control.SetCursorPos(pos)
				Control.KeyDown(spell)
				Control.KeyUp(spell)
				DelayAction(LeftClick,delay/1000,{castSpell.mouse})
				castSpell.casting = ticker + 500
			end
		end
	end
end

function XinZhao:Combo()
    if self:CanCast(_E) then 
		local ETarget = CurrentTarget(650)
		if self.Menu.ComboMode.UseE:Value() and ETarget and ETarget.pos:DistanceTo(myHero.pos) > 200 then
			if ValidTarget(ETarget, 650) then
				Control.CastSpell(HK_E, ETarget)
			end
		end
	end
    if self:CanCast(_W) then 
		local WTarget = CurrentTarget(375)
		if self.Menu.ComboMode.UseW:Value() and WTarget then
			if ValidTarget(WTarget, 275) then
				Control.CastSpell(HK_W)
			end
		end
	end
	if self:CanCast(_Q) then
		local QTarget = CurrentTarget(375)
		if self.Menu.ComboMode.UseQ:Value() and QTarget and myHero.attackData.state == STATE_WINDDOWN then
            if ValidTarget(QTarget, 275) and not self:CanCast(_E) and not self:CanCast(_W) then
				Control.CastSpell(HK_Q)
            end
		end
	end

    if self:CanCast(_R) then
        local RCTarget = CurrentTarget(270)
        if self.Menu.ComboMode.UseR:Value() and RCTarget and not self:CanCast(_E) then
            if myHero.pos:DistanceTo(RCTarget.pos) < 180 and not self:CanCast(_Q) then
			    Control.CastSpell(HK_R)
            end
        end
    end
    
end

function XinZhao:GetEnemyHeroes()
	self.EnemyHeroes = {}
	for i = 1, Game.HeroCount() do
		local Hero = Game.Hero(i)
		if Hero.isEnemy then
			table.insert(self.EnemyHeroes, Hero)
		end
	end
	return self.EnemyHeroes
end

function XinZhao:EnemyInRange(range)
	local count = 0
	for i, target in ipairs(self:GetEnemyHeroes()) do
		if target.pos:DistanceTo(myHero.pos) < range then 
			count = count + 1
		end
	end
	return count
end

function XinZhao:Harass()
    if self:CanCast(_E) then 
		local ETarget = CurrentTarget(650)
		if self.Menu.HarassMode.UseE:Value() and ETarget and ETarget.pos:DistanceTo(myHero.pos) > 200 then
			if ValidTarget(ETarget, 650) then
				Control.CastSpell(HK_E, ETarget)
			end
		end
	end
    if self:CanCast(_W) then 
		local WTarget = CurrentTarget(275)
		if self.Menu.HarassMode.UseW:Value() and WTarget then
			if ValidTarget(WTarget, 275) then
				Control.CastSpell(HK_W)
			end
		end
	end
	if self:CanCast(_Q) then 
		local QTarget = CurrentTarget(275)
		if self.Menu.HarassMode.UseQ:Value() and QTarget and myHero.attackData.state == STATE_WINDDOWN then
            if ValidTarget(QTarget, 275) and not self:CanCast(_E) and not self:CanCast(_W) then
				Control.CastSpell(HK_Q)
            end
		end
	end
end

